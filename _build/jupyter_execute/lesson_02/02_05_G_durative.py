# ## Theme Vowel
# The theme vowel in the durative pattern Râ‚aRâ‚‚Râ‚‚vRâ‚ƒ is a short one. They are the same as in the G preterite, with the only difference that in the theme vowel **u** of the preterite the durative can have an **a** or **u**. It is thus called **vowel class**.
# 
# ## Vowel Classes
# There are four vowel classes:

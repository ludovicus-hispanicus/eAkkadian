# The iconic character of the original sign is visible. The Sumerian word for water is /a/. In this use it can be called a logogram, a pictogram or sometimes a sumerogram. It was turned clockwise 90Â° and became a symbolic sign that could also have a phonetic value. In this case /a/. Over time it acquired new phonetic and logographic values. In this case up to 24 phonetic values and 3 logographic values (if we count the cases where it is the first part of a composite sign, the number of possibilities is even higher). This is representative for all cuneiform signs. They can have more than one phonetic and logographic value.

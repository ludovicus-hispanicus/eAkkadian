# The texts were written on clay, and they just contained personal names and lists of goods. The identification of a language behind these ideograms is problematic, although it is very probable that it was Sumerian (Krebernik 2022, 1 fn. 1). The first understandable texts come from Ur from around the 28th century BCE. They are undoubtedly written in Sumerian. Already in these texts a Semitic name, very likely an Akkadian name, is attested.(Sallbaerger 2004, 108) This shows the coexistence of these two languages from almost the beginning of written Mesopotamian history. This form of writing together with the Akkadian language spread throughout Mesopotamia and became dominant in the ancient Near East until the 1st Millennium BCE, when they were gradually replaced by the Aramaic language and its alphabetic script (Streck 2007, 47). Not only Sumerian and Akkadian used the cuneiform script, but also Hittite, Hurrian, Elamite, Ugaritic and several other smaller languages around the ancient Near East.
# 
# ## Development
# At the beginning the signs were written, drawing them on wet clay tablets. At some point in the third millennium, in the Sargonic Period, between 2334-2193 BCE in middle chronology terms (Studevent-Hickman 2007, 499) the script was turned 90Â° clockwise, the drawing lines were divided in strokes pressed on the wet clay tablet, losing in this way the iconic character and becoming symbolic signs. Following a rebus principle the signs began to represent sounds, but they kept their iconic use, i.e. they could be used for representing those objects for which they were invented. 
# 
# The iconic character of the original sign is visible. The Sumerian word for water is /a/. In this use it can be called a logogram, a pictogram or sometimes a sumerogram. It was turned clockwise 90Â° and became a symbolic sign that could also have a phonetic value. In this case /a/. Over time it acquired new phonetic and logographic values. In this case up to 24 phonetic values and 3 logographic values (if we count the cases where it is the first part of a composite sign, the number of possibilities is even higher). This is representative for all cuneiform signs. They can have more than one phonetic and logographic value.

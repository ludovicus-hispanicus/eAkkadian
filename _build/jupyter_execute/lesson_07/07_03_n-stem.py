# ## Morphological Characteristics
# 
# 1. The N-stem is characterized  by an *n* before the root.
# 2. Forms with prefixes the *n* stand before another consonant, thus it assimilates:
# 
#    |               | N-Stem      | 
#    | -------       | -------------- | 
#    | Durative      | *ipparras* <**inparras*
#    | Perfect       | *ittapras* <**intapras*
#    | Preterite     | *ipparis* <**inparis*
#    | Particple     | *mupparsum* <**munparsum*
# 
# 
# 3. The other form begin with *na*, thus there is no assimilation:
# 
#    |               | N-Stem      | 
#    | -------       | -------------- | 
#    | Imperative    | *napris* 
#    | Verbal Adjective | *naprusum*
#    | Infinitve     | *naprusum*
#    | Stative       | *naprus*
# 
# 
# 4. Because the N-stem is based on the G-stem the personal prefixes are the same, unlike the Å  and D-stems.
# 5. The vowel class depends in part of that of the G-stem
#    1. Vowel class *a-u*, *a* and *u* â†’ N-stem is *a-i*

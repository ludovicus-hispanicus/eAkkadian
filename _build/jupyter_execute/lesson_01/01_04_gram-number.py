# (numerus)=
# # L1.4: Grammatical Number
# There are thre numbers: 
# 
# - **Singular**: It has no special ending.
# - **dual**: It is marked by the ending *-ﾄ］* in nominative (*ﾄｫn* in acc.-gen.). Already in Old Babylonian the dual is not being used anymore. Its use is restricted to parts of the body that are pairs, numbers and some other nouns. 
#  
#     ```{warning}
#     Adjectives have no dual forms. For more details see ABG ﾂｧ144 and AG 2.1(f).
#     ```
# 
# - **plural**: For the masculine it is marked by the endings: *-ﾅｫ* (nominative) and *-ﾄｫ* in acc.-gen.; for the feminine nouns and adjectives *-ﾄ》*. The masculine adjective is marked by *-ﾅｫt*
#   
# 
# | Number | Gender | Case | Substantive | Adjective |
# |------- | ------ | -----| ----------- | --------- |
# | sg.    |  masc. | nom. | *ﾅ｡arrum*    | *dannum*  |
# |        |        | gen. | *ﾅ｡arrim*    | *dannim*  |
# |        |        | acc. | *ﾅ｡arram*    | *dannam*  |
# | dual   |  masc. | nom. | *ﾅ｡arrﾄ］*      | -         |
# |        |        | gen./acc. | *ﾅ｡arrﾄｫn* | -         |        
# | pl.    | masc.  | nom. | *ﾅ｡arrﾅｫ*       | *dannﾅｫtum*  |
# |        |        | gen./acc. | *ﾅ｡arrﾄｫ*  | *dannﾅｫtim*  |
# ||||||
# | sg.    |  fem   | nom. | *ﾅ｡arratum*      | *dannatum* |
# |        |        | gen. | *ﾅ｡arratim*      | *dannatim* |
# |        |        | acc. | *ﾅ｡arratam*      | *dannatam* |
# | dual   |  fem.  | nom. | *ﾅ｡arratﾄ］*      |  |
# |        |        | gen./acc. | *ﾅ｡arratﾄｫn* |  |        

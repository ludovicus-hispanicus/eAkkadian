# (casus)=
# ## Grammatical Case
# In Akkadian there are three grammatical cases: 
# 
# - The **nominative** indicates the subject in verbal and nominal sentences. It has the following endings:
#   - sg. *-um*
#   - du. *-ﾄ］*
#   - m.pl. *-ﾅｫ* 
#   - f.pl. *-um* 
#   - adj.pl. *-um* 
# 
# 
#   |  Number     | Ending    | Example   | Translation |
#   | ---------   | -------   | -------   | ----------- |
#   | sg.         | *-um*     | *ﾅ｡arr-um*  | king        |
#   | du.         | *-ﾄ］*     | *in-ﾄ］*    | (both) eyes |
#   | m.pl.       | *-ﾅｫ*      | *il-ﾅｫ*     | gods        |
#   | f.pl.       | *-um*     | *ﾅ｡arrﾄ》-um* | queens    |
#   | adj.pl.     | *-um*     | *盪ｭabﾅｫt-um*  | good      |
# 
# - The **genitive** indicates possession. It is used after a prepostition and accompanies the *status constructus*. It has the following endings:
#   - sg. *-im*
#   - du. *-ﾄｫn*
#   - n.pl. *-ﾄｫ*; 
#   - n.f.pl. *-im*
#   - adj.pl. *-im*
#   
#   |  Number     | Ending    | Example   | 
#   | ---------   | -------   | -------   | 
#   | sg.         | *-im*     | *ﾅ｡arr-im*  | 
#   | du.         | *-ﾄｫn*     | *in-ﾄｫn*    | 
#   | n.pl.       | *-ﾄｫ*      | *il-ﾄｫ*     | 
#   | n.f.pl.     | *-im*     | *ﾅ｡arrﾄ》-im* | 
#   | adj.pl.     | *-im*     | *盪ｭabﾅｫt-im*  | 
# 
# - The **accusative:** indicates the direct object of the verbs. It is also used in several adverbial expressions. It has the endings:
#   - sg. *-am*
#   - du. *-ﾄｫn*
#   - n.pl. *-ﾄｫ*; 
#   - f.pl. *-im*
#   - adj.pl. *-im*
# 
#   |  Number     | Ending    | Example   | 
#   | ---------   | -------   | -------   | 
#   | sg.         | *-am*     | *ﾅ｡arr-im*  | 
#   | du.         | *-ﾄｫn*     | *in-ﾄｫn*    | 
#   | n.pl.       | *-ﾄｫ*      | *il-ﾄｫ*     | 
#   | n.f.pl.     | *-im*     | *ﾅ｡arrﾄ》-im* | 
#   | adj.pl.     | *-im*     | *盪ｭabﾅｫt-im*  |
# 
# 
#     ```{warning}
#     The genitive and accusative in dual and plural have the same endings. It is also called ***casus obliquus*** or **gen.-acc.**
#     ```
# 
# ```{note}
# There are two more cases that already in Old Babylonian are not productive anymore: 
# - The **locative** has the ending *-um*. It is replaced by the preposition *ina* + genitiv. It is used mainly in frozen adverial phrases. It can be used with pronominal suffixes. 
# - The **terminative** has the ending *-iﾅ｡*. It is replaced by the preposition *ana* + genitive.
# 
# |  Case     | Noun    | Femenine |
# | --------- | ------- | ------- |
# | loc.-adv. | *qatum*  | into the hand |  
# | loc.-adv. | *rittﾅｫﾅ｡ﾅ｡a* (\<*ritt-um-ﾅ｡a*) | into her hand |
# | ter.-adv. | *aﾅ｡ar-iﾅ｡* | to the place |
# 
# For more information 汨 GoA 28.2 and 28.3.
# ```
# 
# (gender)=
# ## Grammatical Gender
# There are two grammatical genders: masculine and feminine.
# 
# - **Masculine** 
#   - sg. no special ending.
#   - pl. no special ending. 
# 
# - **Feminine**
#   - sg./du. *-t-* or *-at-*
#   - pl. *-ﾄ》-*  
# 
#   ```{admonition} Good-to-Know
#   :class: tip
#   The form of the feminine is actually *-at-* but the short vowel *a* is elided:
#   - *ﾅ｡ubtu* < *ﾅ｡ub-at-u 
# 
#   In the status constructus the *a* reappears. 汨閏L1.3: Status constructus - Part 1](01_03_status-constructus)
#   ```
# 
# | Gender | Number | Case      | Substantive |
# |------- | ------ | -----     | ----------- |
# | m.     | sg.    | nom.      | *ﾅ｡arr-um*   |
# | m.     | pl.    | nom.      | *ﾅ｡arr-ﾅｫ*   |
# | f.     | sg.    | nom.      | *ﾅ｡arr-at-um*  |
# | f.     | pl.    | nom.      | *ﾅ｡arr-ﾄ》-um*  |
# 
# ```{note}
# Some formally masculine nouns are gramatically femenine : 
# 
# | Feminine | Translation |
# |--------- | ----------- |
# | *arhum*  | cow         |
# | *ﾅ｡ﾄ菟um*  | foot        |
# | *ummum*  | mother      |
# | *enzum*  | she-goat    |
# | *nﾄ〉um*  | river       |
# 
# For more exception to this rule 汨烏oA 2.1(e).
# ```
# (numerus)=
# ## Grammatical Number
# There are thre numbers: 
# 
# - **Singular**: no special ending.
# - **dual**: no special ending.
# 
#     ```{note}
#     Already in Old Babylonian the dual is not being used anymore. Its use is restricted to parts of the body that are pairs, numbers and some other nouns.
#     ```
# - **plural**: 
#   - adj.m. *-ﾅｫt-* 
#   - n.f. *-ﾄ》*.
#   - adj.f.  *-ﾄ》*.
#   
#     | Gender | Number | Case      | Substantive |
#     |------- | ------ | -----     | ----------- |
#     | m.     | sg.    | nom.      | *dann-ﾅｫt-um*   |
#     | f.     | pl.    | nom.      | *ﾅ｡arr-ﾄ》-um*  | 
#     | m.     | sg.    | nom.      | *dann-ﾄ》-um*   |
# 
# ## Paradigm of the Masculine Noun and Adjective
# | Number | Case      | Substantive | Adjective     | 
# |------- | -----     | ----------- | ---------     |
# | sg.    | nom.      | *ﾅ｡arr-um*   | *dann-um*     |  
# |        | gen.      | *ﾅ｡arr-im*   | *dann-im*     |  
# |        | acc.      | *ﾅ｡arr-am*   | *dann-am*     |    
# | dual   | nom.      | *ﾅ｡arr-ﾄ］*   | *dann-ﾅｫt-um*   |    
# |        | gen./acc. | *ﾅ｡arr-ﾄｫn*   | *dann-ﾅｫt-im*   |        
# | pl.    | nom.      | *ﾅ｡arr-ﾅｫ*    | *dann-ﾅｫt-um*  |   
# |        | gen./acc. | *ﾅ｡arr-ﾄｫ*    | *dann-ﾅｫt-im*  |  
# 
# 
# ## Paradigm of the Feminine Noun and Adjective
# | Number | Case      | Substantive       | Adjective    |
# |------- | -----     | -----------       | ---------    |
# | sg.    | nom.      | *ﾅ｡arr-at-um*      | *dann-at-um* |
# |        | gen.      | *ﾅ｡arr-at-im*      | *dann-at-im* |         
# |        | acc.      | *ﾅ｡arr-at-am*      | *dann-at-am* |
# | dual   | nom.      | *ﾅ｡arr-at-ﾄ］*      | *dann-ﾄ》-um* |
# |        | gen./acc. | *ﾅ｡arr-at-ﾄｫn*      | *dann-ﾄ》-im* |
# | pl.    | nom.      | *ﾅ｡arr-ﾄ》-um*      | *dann-ﾄ》-um* |
# |        | gen./acc. | *ﾅ｡arr-ﾄ》-im*      | *dann-ﾄ》-im* |
